s = input("Введіть рядок:")
lenght = len(s)
print("третій символ рядка - ", s[2])
print("передостанній символ рядка - ", s[lenght - 2])
print("крім 2-х останніх символів рядка - ", s[:lenght - 2])
print("перші 5 символів рядка - ", s[:5])
print("парні символи рядка - ", s[1::2])
print("непарні символи рядка - ", s[::2])
print("зворотній порядок - ", s[lenght::-1])
print("зворотній порядок (через один) - ", s[lenght::-2])
print("довжина рядка", lenght)
print("\u2764")